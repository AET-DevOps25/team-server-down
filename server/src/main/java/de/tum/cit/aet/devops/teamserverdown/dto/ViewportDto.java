package de.tum.cit.aet.devops.teamserverdown.dto;

public class ViewportDto {
  private Double x;
  private Double y;
  private Double zoom;

  public Double getX() {
    return x;
  }

  public void setX(Double x) {
    this.x = x;
  }

  public Double getY() {
    return y;
  }

  public void setY(Double y) {
    this.y = y;
  }

  public Double getZoom() {
    return zoom;
  }

  public void setZoom(Double zoom) {
    this.zoom = zoom;
  }
}
